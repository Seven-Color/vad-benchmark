import json
import os
import sys
import warnings
warnings.filterwarnings("ignore")

import numpy as np
import soundfile as sf
import torch  # for silero frame-level scoring

# ─────────────────────────────────────────────
# 0. Paths
# ─────────────────────────────────────────────
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
TEST_AUDIO_DIR = os.path.join(BASE_DIR, "test_labeled")   # flat directory
RESULTS_FILE = os.path.join(BASE_DIR, "results.json")

FRAME_MS = 10
SAMPLE_RATE = 16000
FRAME_SAMPLES = int(SAMPLE_RATE * FRAME_MS / 1000)

def find_labeled_wavs(base_dir):
    """Scan a flat directory for .wav files whose .labels.json sibling exists."""
    wav_files = []
    for f in os.listdir(base_dir):
        if f.endswith('.wav'):
            lbl = os.path.join(base_dir, f.replace('.wav', '.labels.json'))
            if os.path.exists(lbl):
                wav_files.append(os.path.join(base_dir, f))
    return sorted(wav_files)

# ─────────────────────────────────────────────
# Metrics helpers
# ─────────────────────────────────────────────
def segments_to_frames(segs, audio_len_s, sr, frame_ms=10):
    frame_samples = int(sr * frame_ms / 1000)
    n_frames = int(audio_len_s * 1000 / frame_ms)
    frames = np.zeros(n_frames, dtype=np.int8)
    for s in segs:
        start_f = max(0, int(s["start"] * 1000 / frame_ms))
        end_f = min(n_frames, int(s["end"] * 1000 / frame_ms))
        frames[start_f:end_f] = 1
    return frames

def compute_metrics(y_true, y_pred):
    tp = int(np.sum((y_true == 1) & (y_pred == 1)))
    tn = int(np.sum((y_true == 0) & (y_pred == 0)))
    fp = int(np.sum((y_true == 0) & (y_pred == 1)))
    fn = int(np.sum((y_true == 1) & (y_pred == 0)))
    acc  = (tp + tn) / max(1, tp + tn + fp + fn)
    prec = tp / max(1, tp + fp)
    rec  = tp / max(1, tp + fn)
    f1   = 2 * prec * rec / max(1e-9, prec + rec)
    far  = fp / max(1, fp + tn)
    frr  = fn / max(1, fn + tp)
    return {"accuracy": round(acc, 4), "precision": round(prec, 4),
            "recall": round(rec, 4), "f1": round(f1, 4),
            "false_alarm_rate": round(far, 4), "miss_rate": round(frr, 4),
            "tp": tp, "tn": tn, "fp": fp, "fn": fn}

def compute_roc_curve(y_true, y_scores, points=100):
    thresholds = np.linspace(y_scores.min(), y_scores.max(), points)
    fprs, tprs = [], []
    for t in thresholds:
        y_pred = (y_scores >= t).astype(np.int8)
        tp = int(np.sum((y_true == 1) & (y_pred == 1)))
        fp = int(np.sum((y_true == 0) & (y_pred == 1)))
        fn = int(np.sum((y_true == 1) & (y_pred == 0)))
        tn = int(np.sum((y_true == 0) & (y_pred == 0)))
        fpr = fp / max(1, fp + tn)
        tpr = tp / max(1, tp + fn)
        fprs.append(fpr)
        tprs.append(tpr)
    auc = float(np.trapezoid(tprs, fprs))
    return fprs, tprs, round(auc, 4)

# ─────────────────────────────────────────────
# Ground truth
# ─────────────────────────────────────────────
def load_labels(wav_path):
    label_path = wav_path.replace(".wav", ".labels.json")
    if os.path.exists(label_path):
        with open(label_path, "r") as f:
            data = json.load(f)
        labels = np.array(data["labels"], dtype=np.int8)
        print(f"  [GT] Loaded ground truth: {os.path.basename(label_path)}")
        return labels, "ground_truth"
    return None, None

def energy_based_labels(audio, sr, frame_ms=10):
    from scipy.signal import butter, lfilter
    frame_samples = int(sr * frame_ms / 1000)
    hop = frame_samples
    low  = max(300 / sr, 0.01)
    high = min(3400 / sr, 0.99)
    if high <= low:
        low, high = 0.01, 0.99
    b, a = butter(4, [low, high], btype='band')
    filtered = lfilter(b, a, audio)
    energies = []
    for i in range(0, len(filtered) - frame_samples + 1, hop):
        e = np.sqrt(np.mean(filtered[i:i+frame_samples]**2))
        energies.append(e)
    energies = np.array(energies)
    thresh = np.mean(energies) + 0.5 * np.std(energies)
    binary = (energies > thresh).astype(np.int8)
    smoothed = binary.copy()
    for i in range(2, len(binary)):
        if binary[i] == 1 or (binary[i-1] == 1 and binary[i-2] == 1):
            smoothed[i] = 1
        elif binary[i-1] == 0 and binary[i-2] == 0:
            smoothed[i] = 0
    n = int(len(audio) / hop)
    return smoothed[:n]

# ─────────────────────────────────────────────
# Silero VAD
# ─────────────────────────────────────────────
def run_silero(wav_path):
    try:
        sys.path.insert(0, os.path.join(BASE_DIR, "silero-vad", "src"))
        from silero_vad import load_silero_vad, get_speech_timestamps
        import torch
        torch.set_num_threads(1)

        model = load_silero_vad(onnx=False)
        audio, sr = sf.read(wav_path)
        wav_tensor = torch.from_numpy(audio).float()
        if wav_tensor.ndim == 1:
            wav_tensor = wav_tensor.unsqueeze(0)

        stamps = get_speech_timestamps(wav_tensor, model, sampling_rate=sr, return_seconds=True)
        frames = segments_to_frames(stamps, len(audio) / sr, sr, FRAME_MS)

        return {"method": "Silero VAD", "model_type": "torch",
                "speech_segments": len(stamps), "frames": frames,
                "model": model, "audio": wav_tensor, "sr": sr,
                "audio_file": wav_path}
    except Exception as e:
        return {"method": "Silero VAD", "model_type": "torch", "error": str(e),
                "frames": np.zeros(1, dtype=np.int8)}

# ─────────────────────────────────────────────
# WebRTC VAD
# ─────────────────────────────────────────────
def run_webrtc(wav_path):
    try:
        from webrtcvad import Vad
        from scipy.signal import resample

        audio, sr = sf.read(wav_path)
        if sr != SAMPLE_RATE:
            target_len = int(len(audio) * SAMPLE_RATE / sr)
            audio = resample(audio, target_len)
            sr = SAMPLE_RATE

        vad = Vad(2)
        frame_size = FRAME_SAMPLES
        frames = []
        for i in range(0, len(audio), frame_size):
            frame = audio[i:i+frame_size]
            if len(frame) < frame_size:
                frame = np.pad(frame, (0, frame_size - len(frame)))
            pcm = (np.clip(frame, -1.0, 1.0) * 32767).astype(np.int16).tobytes()
            frames.append(int(vad.is_speech(pcm, sr)))

        return {"method": "WebRTC VAD", "model_type": "c-extension",
                "speech_segments": sum(1 for i in range(1, len(frames))
                                       if frames[i] and not frames[i-1]),
                "frames": np.array(frames, dtype=np.int8),
                "audio_file": wav_path}
    except Exception as e:
        return {"method": "WebRTC VAD", "model_type": "c-extension", "error": str(e),
                "frames": np.zeros(1, dtype=np.int8)}

# ─────────────────────────────────────────────
# Energy VAD
# ─────────────────────────────────────────────
def run_energy_vad(wav_path):
    try:
        from scipy.signal import butter, lfilter, resample

        audio, sr = sf.read(wav_path)
        if sr != SAMPLE_RATE:
            target_len = int(len(audio) * SAMPLE_RATE / sr)
            audio = resample(audio, target_len)
            sr = SAMPLE_RATE

        b, a = butter(5, [300/sr, 3000/sr], btype='band')
        filtered = lfilter(b, a, audio)

        hop = FRAME_SAMPLES
        energies = []
        for i in range(0, len(filtered) - hop + 1, hop):
            e = np.sqrt(np.mean(filtered[i:i+hop]**2))
            energies.append(e)
        energies = np.array(energies)

        thresh = np.mean(energies) + 0.8 * np.std(energies)
        binary = (energies > thresh).astype(np.int8)
        smoothed = binary.copy()
        for i in range(1, len(binary)):
            smoothed[i] = 1 if (binary[i-1:i+1].sum() >= 2) else 0

        n_frames = int(len(audio) / hop)
        frames = smoothed[:n_frames]

        segs = []
        in_speech = False
        start = 0
        for i, f in enumerate(frames):
            t = i * hop / sr
            if f and not in_speech:
                start = t
                in_speech = True
            elif not f and in_speech:
                segs.append({"start": round(start, 3), "end": round(t, 3)})
                in_speech = False
        if in_speech:
            segs.append({"start": round(start, 3), "end": round(len(audio)/sr, 3)})

        return {"method": "Energy VAD", "model_type": "energy",
                "speech_segments": len(segs), "frames": frames,
                "scores": np.clip(energies / (thresh + 1e-9), 0, 3)[:n_frames],
                "audio_file": wav_path}
    except Exception as e:
        return {"method": "Energy VAD", "model_type": "energy", "error": str(e),
                "frames": np.zeros(1, dtype=np.int8)}

# ─────────────────────────────────────────────
# Silero frame-level probability
# ─────────────────────────────────────────────
def get_silero_scores_from_result(result, frame_ms=10):
    if "model" not in result or "audio" not in result:
        return None
    model = result["model"]
    audio = result["audio"]
    sr = result["sr"]
    window_samples = 512
    hop = int(sr * frame_ms / 1000)
    scores = []
    with torch.no_grad():
        for i in range(0, audio.shape[-1] - window_samples + 1, hop):
            chunk = audio[:, i:i+window_samples]
            prob = model(chunk, sr).item()
            scores.append(prob)
    return np.array(scores)

# ─────────────────────────────────────────────
# Evaluate
# ─────────────────────────────────────────────
def evaluate_wav(wav_path):
    audio, sr = sf.read(wav_path)
    audio_len_s = len(audio) / sr
    n_frames = int(audio_len_s * 1000 / FRAME_MS)

    gt_labels, ref_type = load_labels(wav_path)
    if gt_labels is None:
        print(f"  [GT] No labels found — using energy-based pseudo-labels (unreliable)")
        ref_type = "energy_pseudo"
        ref = energy_based_labels(audio, sr, FRAME_MS)
        ref = ref[:n_frames] if len(ref) >= n_frames else np.pad(ref, (0, n_frames - len(ref)))
    else:
        ref = gt_labels[:n_frames] if len(gt_labels) >= n_frames else np.pad(gt_labels, (0, n_frames - len(gt_labels)))

    silero_r = run_silero(wav_path)
    webrtc_r = run_webrtc(wav_path)
    energy_r = run_energy_vad(wav_path)

    results = []
    for r in [silero_r, webrtc_r, energy_r]:
        if "error" in r:
            print(f"  ERROR {r['method']}: {r['error']}")
            continue

        frames = r["frames"]
        frames = frames[:n_frames] if len(frames) >= n_frames else np.pad(frames, (0, n_frames - len(frames)))

        metrics = compute_metrics(ref, frames)
        total_dur = round(float(np.sum(frames)) * FRAME_MS / 1000, 2)

        entry = {
            "method": r["method"],
            "model_type": r["model_type"],
            "reference": ref_type,
            "speech_segments": r["speech_segments"],
            "speech_duration_s": total_dur,
            "audio_file": wav_path,
            **metrics
        }

        # ROC
        if r["method"] == "Silero VAD":
            scores = get_silero_scores_from_result(r, FRAME_MS)
            if scores is not None and len(scores) > 0:
                scores = scores[:n_frames] if len(scores) >= n_frames else np.pad(scores, (0, n_frames - len(scores)))
                scores = scores[:n_frames]
                fprs, tprs, auc = compute_roc_curve(ref, scores)
                metrics["auc"] = auc
                entry["roc"] = {"fpr": fprs, "tpr": tprs, "auc": auc}
        elif r["method"] == "Energy VAD" and "scores" in r:
            sc = r["scores"]
            sc = sc[:n_frames] if len(sc) >= n_frames else np.pad(sc, (0, n_frames - len(sc)))
            sc = sc[:n_frames]
            fprs, tprs, auc = compute_roc_curve(ref, sc)
            metrics["auc"] = auc
            entry["roc"] = {"fpr": fprs, "tpr": tprs, "auc": auc}

        results.append(entry)
        auc_str = f" AUC={metrics.get('auc', 'N/A')}" if "auc" in metrics else ""
        print(f"  {r['method']} ({r['model_type']}): "
              f"Acc={metrics['accuracy']} Prec={metrics['precision']} "
              f"Rec={metrics['recall']} F1={metrics['f1']}{auc_str}")

    return results

# ─────────────────────────────────────────────
# Plot
# ─────────────────────────────────────────────
def plot_roc(results, output_path):
    try:
        import matplotlib
        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
    except ImportError:
        print("  matplotlib not available — skipping ROC plot")
        return

    fig, ax = plt.subplots(figsize=(7, 5))
    seen = {}
    for r in results:
        if r.get("roc"):
            key = r['method']
            if key not in seen:
                seen[key] = r
                auc = r['roc']['auc']
                label = f"{r['method']} (AUC={auc:.3f})"
                ax.plot(r['roc']["fpr"], r['roc']["tpr"], label=label, linewidth=2)
                ax.fill_between(r['roc']["fpr"], r['roc']["tpr"], alpha=0.08)

    ax.plot([0, 1], [0, 1], "k--", alpha=0.4, label="Random baseline")
    ax.set_xlabel("False Alarm Rate (FPR)")
    ax.set_ylabel("Recall (TPR)")
    ax.set_title("VAD Benchmark — ROC Curves (all conditions)")
    ax.legend(fontsize=9)
    ax.grid(True, alpha=0.3)
    ax.set_xlim([0, 1])
    ax.set_ylim([0, 1])

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    print(f"\nROC curves saved to {output_path}")
    plt.close()

