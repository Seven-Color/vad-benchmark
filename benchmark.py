# Main
# ─────────────────────────────────────────────
def main():
    print("=" * 60)
    print("VAD Benchmark — Frame-level Evaluation")
    print(f"Frame size: {FRAME_MS}ms | Sample rate: {SAMPLE_RATE}Hz")
    print("=" * 60)

    # Load all labeled test files from flat test_labeled/ directory
    test_files = find_labeled_wavs(TEST_AUDIO_DIR)
    if not test_files:
        print(f"\n[ERROR] No labeled .wav files found in {TEST_AUDIO_DIR}")
        print("  Run: python generate_synthetic_data.py")
        return

    print(f"\nFound {len(test_files)} labeled test files")
    results = []

    for wav in test_files:
        label_file = wav.replace('.wav', '.labels.json')
        with open(label_file, 'r') as f:
            meta = json.load(f)
        noise_type = meta.get('noise_type', 'none')
        snr = meta.get('snr_db')
        condition = f"{noise_type}_SNR{snr}" if snr is not None else "clean"
        print(f"\n=== [{condition}] {os.path.basename(wav)} ===")
        print(f"  Duration: {meta.get('duration_s')}s, Speech: {meta.get('speech_frac', 'N/A')}")
        print(f"  Noise: {noise_type}, SNR: {snr}dB")
        entry_results = evaluate_wav(wav)
        for entry in entry_results:
            entry['condition'] = condition
            entry['noise_type'] = noise_type
            entry['snr_db'] = snr
        results.extend(entry_results)

    with open(RESULTS_FILE, "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2, ensure_ascii=False)
    print(f"\nResults saved to {RESULTS_FILE}")

    # Summary table
    print("\n" + "=" * 80)
    print(f"{'Method':<20} {'Condition':<18} {'Acc':>6} {'Prec':>6} {'Rec':>6} {'F1':>6} {'AUC':>6}")
    print("-" * 80)
    for r in results:
        cond = r.get('condition', '?')
        auc = r.get('auc', 'N/A')
        auc_str = f"{auc:.4f}" if isinstance(auc, float) else str(auc)
        print(f"{r['method']:<20} {cond:<18} "
              f"{r['accuracy']:>6.4f} {r['precision']:>6.4f} "
              f"{r['recall']:>6.4f} {r['f1']:>6.4f} {auc_str:>6}")

    roc_path = os.path.join(BASE_DIR, "roc_curves.png")
    plot_roc(results, roc_path)

if __name__ == "__main__":
    main()